public class Facil{
    private String palabra, muestra;

    public Facil(){
        this.palabra = palabra;
        this.muestra = muestra;
    }

    public String getPalabra(){
        return palabra;
    }
    public void setPalabra(String palabra){
        this.palabra = palabra;
    }

    public String getMuestra(){
        return muestra;
    }
    public void setMuestra(String muestra){
        this.muestra = muestra;
    }
}